"""
ingestion/chunk.py
------------------
Section-first chunking pipeline (parse_pdf-based architecture).

Flow:
1) Load mapped section seeds from data/processed/raw_sections_ot.jsonl
   (generated by ingestion.extract using ingestion.parse_pdf).
2) Apply semantic chunking WITHIN each section independently.
3) Enforce max token budget per chunk.
4) Add sentence-overlap chunks across adjacent chunks in the same chapter.
5) Run 9 validation tests (including overlap and section-level checks).

This script intentionally does NOT run propositions.py.
"""

