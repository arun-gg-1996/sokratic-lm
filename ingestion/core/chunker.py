def main(threshold: int = 90, out_path: str = CHUNKS_PATH, run_tests: bool = True) -> int:
    sections = load_raw_sections(RAW_SECTIONS_PATH)
    print(f"Loaded section seeds: {len(sections)} from {RAW_SECTIONS_PATH}")

    base_chunks = semantic_chunk_sections(
        sections,
        threshold=threshold,
        max_chunk_tokens=MAX_CHUNK_TOKENS,
    )
    print(f"Base semantic chunks: {len(base_chunks)}")

    base_chunks, merged_count = merge_pronoun_start_chunks(
        base_chunks,
        max_chunk_tokens=MAX_CHUNK_TOKENS,
    )
    print(f"Base chunks after pronoun-merge: {len(base_chunks)} (merged={merged_count})")

    all_chunks = add_overlap_chunks(
        base_chunks,
        overlap_sentences=OVERLAP_SENTENCE_COUNT,
        max_chunk_tokens=MAX_CHUNK_TOKENS,
    )
    print(f"Total chunks after overlap: {len(all_chunks)}")

    # Parallel table extraction branch (no overlap for table chunks)
    table_chunks, table_stats = extract_table_chunks(
        pdf_path=cfg.paths.raw_ot_pdf,
        section_rows=sections,
    )
    print(
        "Table extraction:",
        f"total_in_pdf={table_stats['total_tables_in_pdf']}",
        f"filtered_raw={table_stats['filtered_raw']}",
        f"filtered_converted={table_stats['filtered_converted']}",
        f"added={table_stats['table_chunks_added']}",
    )

    all_chunks = normalize_chunk_fields(all_chunks + table_chunks)
    print(f"Total chunks after tables + normalization: {len(all_chunks)}")

    save_chunks(all_chunks, out_path)

    if not run_tests:
        return 0

    report = run_all_tests(all_chunks, sections)

    print("\n" + "=" * 70)
    print("VALIDATION TEST RESULTS")
    print("=" * 70)
    for r in report["results"]:
        status = "PASS" if r.passed else "FAIL"
        print(f"[{status}] {r.name}: {r.details}")

    print("\nTEST 2 — Topic coherence sample (10 chunks)")
    for i, row in enumerate(report["topic_rows"], 1):
        print(f"\n[{i}] {row['flag']} topics={row['distinct_topics']} tokens={row['token_count']} section={row['section_title'][:80]}")
        print(row["text"])

    print("\nTEST 5 — Chapter counts")
    for ch in range(1, 29):
        print(f"Ch{ch:02d}: {report['chapter_counts'][ch]}")

    print("\nTEST 6 — Keyword counts")
    for k, v in report["keyword_counts"].items():
        print(f"{k}: {v}")

    print("\nTEST 7 — Proposition preview")
    for key, payload in report["previews"].items():
        print(f"\n{key}:")
        if "error" in payload:
            print(f"  error: {payload['error']}")
            continue
        print(f"  chunk_id: {payload['chunk_id']}")
        print(f"  element_type: {payload['element_type']}")
        print(f"  text: {payload['text'][:600]}")
        print("  propositions:")
        for p in payload.get("propositions", [])[:12]:
            print(f"    - {p}")

    print("\nTEST 9 — Section-level details")
    print(f"L1 sections: {report['section_payload']['l1']}")
    print(f"L2 sections: {report['section_payload']['l2']}")
    print("Section titles with 'axillary nerve' chunks:")
    for t in report["section_payload"]["axillary_titles"]:
        print(f"- {t}")

    print("\nOverall:", "PASS" if report["all_pass"] else "FAIL")
    return 0 if report["all_pass"] else 1


