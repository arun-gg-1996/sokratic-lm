def _build_splitter(threshold: int) -> SemanticSplitterNodeParser:
    api_key = os.environ.get("OPENAI_API_KEY")
    embed_model = OpenAIEmbedding(
        model="text-embedding-3-small",
        api_key=api_key,
        embed_batch_size=100,
    )
    return SemanticSplitterNodeParser(
        embed_model=embed_model,
        breakpoint_percentile_threshold=threshold,
        buffer_size=1,
    )


def semantic_chunk_sections(
    section_rows: list[dict],
    threshold: int = 90,
    max_chunk_tokens: int = MAX_CHUNK_TOKENS,
) -> list[dict]:
    """Semantic split each section independently."""
    splitter = _build_splitter(threshold)
    encoder = _get_token_encoder()

    base_chunks: list[dict] = []
    seq = 0

    for sec in tqdm(section_rows, desc="Semantic splitting sections"):
        text = (sec.get("text") or "").strip()
        if len(text) < MIN_CHUNK_CHARS:
            continue

        doc = Document(text=text)
        try:
            nodes = splitter.get_nodes_from_documents([doc], show_progress=False)
            node_texts = [(n.text or "").strip() for n in nodes if (n.text or "").strip()]
        except Exception as exc:
            print(f"Splitter fallback on section {sec.get('source_section_id')} due to: {exc}")
            node_texts = [text]

        for node_text in node_texts:
            parts = _split_to_token_budget(node_text, encoder, max_tokens=max_chunk_tokens)
            if not parts:
                continue

            for part in parts:
                seq += 1
                chunk_obj = {
                    "chunk_id": str(uuid.uuid4()),
                    "text": part,
                    "chapter_num": sec["chapter_num"],
                    "chapter_title": sec["chapter_title"],
                    "section_num": sec.get("section_num", ""),
                    "section_title": _sanitize_section_title(
                        sec.get("section_title", ""),
                        chapter_title=sec.get("chapter_title", ""),
                        section_num=sec.get("section_num", ""),
                    ),
                    "subsection_title": sec.get("subsection_title", ""),
                    "page": sec["page"],
                    "element_type": "paragraph",
                    "is_overlap": False,
                    "domain": sec["domain"],
                    "source_section_id": sec.get("source_section_id", ""),
                    "source_level": sec.get("source_level", 1),
                    "sequence_index": seq,
                }
                if _is_noise_chunk(chunk_obj["text"]):
                    continue
                if _token_len(chunk_obj["text"], encoder) < MIN_CHUNK_TOKENS:
                    continue
                base_chunks.append(chunk_obj)

    return base_chunks


def add_overlap_chunks(
    base_chunks: list[dict],
    overlap_sentences: int = OVERLAP_SENTENCE_COUNT,
    max_chunk_tokens: int = MAX_CHUNK_TOKENS,
) -> list[dict]:
    """
    Add overlap chunks: prepend last N sentences from previous chunk to current,
    only within same chapter.
    """
    encoder = _get_token_encoder()
    ordered = sorted(base_chunks, key=lambda c: c.get("sequence_index", 0))

    overlaps: list[dict] = []
    for i in range(1, len(ordered)):
        prev = ordered[i - 1]
        curr = ordered[i]

        if prev["chapter_num"] != curr["chapter_num"]:
            continue

        prefix = _last_n_sentences(prev["text"], overlap_sentences)
        if not prefix:
            continue

        overlap_text = _trim_overlap_to_budget(prefix, curr["text"], encoder, max_chunk_tokens)
        if _is_noise_chunk(overlap_text):
            continue
        if _token_len(overlap_text, encoder) < MIN_CHUNK_TOKENS:
            continue

        overlaps.append({
            **curr,
            "chunk_id": str(uuid.uuid4()),
            "text": overlap_text,
            "element_type": "paragraph_overlap",
            "is_overlap": True,
            "original_chunk_id": curr["chunk_id"],
            "overlap_source_chunk_id": prev["chunk_id"],
            "overlap_prefix": prefix,
            "sequence_index": curr.get("sequence_index", 0) + 0.1,
        })

    return ordered + overlaps


def merge_pronoun_start_chunks(
    base_chunks: list[dict],
    max_chunk_tokens: int = MAX_CHUNK_TOKENS,
) -> tuple[list[dict], int]:
    """
    Merge pronoun-start paragraph chunks into previous paragraph chunk
    within same chapter+section when token budget allows.
    """
    encoder = _get_token_encoder()
    ordered = sorted(base_chunks, key=lambda c: c.get("sequence_index", 0))
    merged: list[dict] = []
    merge_count = 0

    for c in ordered:
        text = c["text"].strip()
        if (
            merged
            and c.get("element_type") == "paragraph"
            and merged[-1].get("element_type") == "paragraph"
            and c.get("chapter_num") == merged[-1].get("chapter_num")
            and c.get("section_num") == merged[-1].get("section_num")
            and PRONOUN_START_RE.match(text)
        ):
            combo = f"{merged[-1]['text'].rstrip()} {text}".strip()
            if _token_len(combo, encoder) <= max_chunk_tokens:
                merged[-1]["text"] = combo
                merge_count += 1
                continue

        merged.append(c)

    return merged, merge_count


def _build_page_section_lookup(section_rows: list[dict]) -> dict[int, dict]:
    """
    Map page -> best matching section metadata.
    Preference:
      1) higher source_level (L2 over L1)
      2) smaller page span (more specific section range)
    """
    lookup: dict[int, dict] = {}
    for s in section_rows:
        start = int(s.get("page", 0))
        end = int(s.get("page_end", start))
        if start <= 0:
            continue
        if end < start:
            end = start

        level = int(s.get("source_level", 1))
        span = end - start + 1
        for p in range(start, end + 1):
            cur = lookup.get(p)
            if cur is None:
                lookup[p] = {**s, "_span": span, "_level": level}
                continue
            if (level > cur["_level"]) or (level == cur["_level"] and span < cur["_span"]):
                lookup[p] = {**s, "_span": span, "_level": level}
    return lookup


def _convert_table_to_text(raw_table: str, client, model: str, prompt_template: str) -> str:
    """Convert a raw table string to prose using Claude with retries."""
    prompt = prompt_template.format(table_text=raw_table)
    for attempt in range(3):
        try:
            msg = client.messages.create(
                model=model,
                max_tokens=900,
                messages=[{"role": "user", "content": prompt}],
            )
            return msg.content[0].text.strip()
        except Exception:
            time.sleep(1.5 * (attempt + 1))
    return ""


def extract_table_chunks(
    pdf_path: str,
    section_rows: list[dict],
) -> tuple[list[dict], dict]:
    """
    Extract tables from PDF, convert to prose, map to section metadata,
    and return ChunkSchema-like table chunks.
    """
    import anthropic
    import pdfplumber

    page_meta = _build_page_section_lookup(section_rows)
    client = anthropic.Anthropic(api_key=os.environ.get("ANTHROPIC_API_KEY"))
    model = cfg.models.propositions
    prompt_template = cfg.prompts.table_to_text

    table_chunks: list[dict] = []
    total_tables = 0
    filtered_raw = 0
    filtered_converted = 0

    with pdfplumber.open(pdf_path) as pdf:
        for page_num, page in enumerate(pdf.pages, start=1):
            tables = page.extract_tables() or []
            total_tables += len(tables)

            for table in tables:
                rows = []
                for row in table or []:
                    cells = [(c or "").replace("\n", " ").strip() for c in row]
                    if any(cells):
                        rows.append(" | ".join(cells))
                raw = "\n".join(rows).strip()
                if not raw:
                    continue

                if _table_reject_reason(raw):
                    filtered_raw += 1
                    continue

                converted = _convert_table_to_text(raw, client, model, prompt_template)
                if not converted:
                    filtered_converted += 1
                    continue

                if _table_reject_reason(converted):
                    filtered_converted += 1
                    continue

                meta = page_meta.get(page_num)
                if meta is None:
                    # fallback metadata when page is not covered by sections
                    chapter_num = 0
                    chapter_title = "Unknown"
                    section_num = ""
                    section_title = "Unknown"
                    subsection_title = ""
                    source_section_id = ""
                    source_level = 1
                else:
                    chapter_num = int(meta.get("chapter_num", 0))
                    chapter_title = meta.get("chapter_title", "")
                    section_num = meta.get("section_num", "") or ""
                    section_title = _sanitize_section_title(
                        meta.get("section_title", ""),
                        chapter_title=chapter_title,
                        section_num=section_num,
                    )
                    subsection_title = meta.get("subsection_title", "")
                    source_section_id = meta.get("source_section_id", "")
                    source_level = int(meta.get("source_level", 1))

                table_chunks.append({
                    "chunk_id": str(uuid.uuid4()),
                    "text": " ".join(converted.split()).strip(),
                    "chapter_num": chapter_num,
                    "chapter_title": chapter_title,
                    "section_num": section_num,
                    "section_title": section_title,
                    "subsection_title": subsection_title,
                    "page": page_num,
                    "element_type": "table",
                    "is_overlap": False,
                    "domain": "ot",
                    "source_section_id": source_section_id,
                    "source_level": source_level,
                    "sequence_index": 10_000_000 + len(table_chunks),
                })

                # light rate-limit pacing
                time.sleep(0.15)

    stats = {
        "total_tables_in_pdf": total_tables,
        "filtered_raw": filtered_raw,
        "filtered_converted": filtered_converted,
        "table_chunks_added": len(table_chunks),
    }
    return table_chunks, stats


def normalize_chunk_fields(chunks: list[dict]) -> list[dict]:
    """Backfill required normalized fields across all chunk types."""
    normalized = []
    for c in chunks:
        c2 = dict(c)
        c2["is_overlap"] = bool(c2.get("element_type") == "paragraph_overlap")
        c2["section_title"] = _sanitize_section_title(
            c2.get("section_title", ""),
            chapter_title=c2.get("chapter_title", ""),
            section_num=c2.get("section_num", ""),
        )
        normalized.append(c2)
    return normalized


def save_chunks(chunks: list[dict], out_path: str = CHUNKS_PATH) -> None:
    Path(out_path).parent.mkdir(parents=True, exist_ok=True)
    with open(out_path, "w", encoding="utf-8") as f:
        for c in chunks:
            f.write(json.dumps(c, ensure_ascii=False) + "\n")
    print(f"Saved {len(chunks)} chunks -> {out_path}")


