@dataclass
class TestResult:
    name: str
    passed: bool
    details: str


def _test_1_token_distribution(chunks: list[dict]) -> TestResult:
    encoder = _get_token_encoder()
    lens = [_token_len(c["text"], encoder) for c in chunks]

    p50 = int(np.percentile(lens, 50))
    p90 = int(np.percentile(lens, 90))
    p95 = int(np.percentile(lens, 95))
    p99 = int(np.percentile(lens, 99))
    mx = max(lens)
    mean = round(float(np.mean(lens)), 2)
    under15 = sum(x < 15 for x in lens)
    over450 = sum(x > 450 for x in lens)

    passed = (p99 < 450 and mx < 512)
    details = (
        f"total={len(chunks)} mean={mean} p50={p50} p90={p90} p95={p95} "
        f"p99={p99} max={mx} under15={under15} over450={over450}"
    )
    return TestResult("TEST 1 — Token distribution", passed, details)


def _estimate_distinct_topics(text: str) -> int:
    sents = _split_sentences(text)
    if len(sents) <= 1:
        return 1
    if len(sents) <= 4:
        return 1

    stop = {
        "the", "and", "for", "that", "with", "from", "this", "these", "those",
        "are", "was", "were", "have", "has", "had", "into", "onto", "over",
        "under", "within", "without", "when", "where", "which", "while", "about",
    }

    def kws(s: str) -> set[str]:
        words = re.findall(r"[A-Za-z][A-Za-z\-]+", s.lower())
        return {w for w in words if len(w) > 3 and w not in stop}

    shifts = 0
    marker_re = re.compile(r"^(however|in contrast|whereas|meanwhile|on the other hand|additionally|in addition|another)\b", re.I)
    prev = kws(sents[0])
    for s in sents[1:]:
        cur = kws(s)
        if not prev and not cur:
            sim = 1.0
        else:
            sim = len(prev & cur) / max(1, len(prev | cur))
        if sim < 0.03 and marker_re.search(s.strip()):
            shifts += 1
        prev = cur

    return 1 + shifts


def _test_2_topic_coherence(chunks: list[dict]) -> tuple[TestResult, list[dict]]:
    paras = [c for c in chunks if c["element_type"] == "paragraph"]
    sample = random.sample(paras, min(10, len(paras)))

    rows = []
    good = 0
    encoder = _get_token_encoder()
    for c in sample:
        distinct = _estimate_distinct_topics(c["text"])
        flag = "GOOD" if distinct == 1 else "BAD"
        if flag == "GOOD":
            good += 1
        rows.append({
            "chunk_id": c["chunk_id"],
            "token_count": _token_len(c["text"], encoder),
            "distinct_topics": distinct,
            "flag": flag,
            "section_title": c.get("section_title", ""),
            "text": c["text"],
        })

    passed = good >= 8
    details = f"GOOD={good}/10 BAD={10-good}/10"
    return TestResult("TEST 2 — Topic coherence", passed, details), rows


def _test_3_pronoun_starts(chunks: list[dict]) -> TestResult:
    p = [c for c in chunks if PRONOUN_START_RE.match(c["text"].strip())]
    pct = (len(p) / len(chunks) * 100.0) if chunks else 0.0
    passed = pct < 5.0
    details = f"count={len(p)} pct={pct:.2f}%"
    return TestResult("TEST 3 — Pronoun-start chunks", passed, details)


def _test_4_noise_detection(chunks: list[dict]) -> TestResult:
    noise = []
    for c in chunks:
        t = " ".join(c["text"].split()).strip()
        tl = t.lower()
        if tl in {"review questions", "learning objectives", "chapter review"}:
            noise.append(c)
            continue
        if re.fullmatch(r"(figure|table)\s+\d+\.\d+\.?", tl):
            noise.append(c)
            continue
        if re.fullmatch(r"[\d\s.,;:()\-/]+", t):
            noise.append(c)
            continue

    passed = len(noise) == 0
    details = f"noise_chunks={len(noise)}"
    return TestResult("TEST 4 — Noise detection", passed, details)


def _test_5_chapter_coverage(chunks: list[dict]) -> tuple[TestResult, dict[int, int]]:
    counts = {ch: 0 for ch in range(1, 29)}
    for c in chunks:
        ch = int(c.get("chapter_num", 0))
        if 1 <= ch <= 28:
            counts[ch] += 1

    missing = [ch for ch, n in counts.items() if n == 0]
    passed = len(missing) == 0
    details = f"missing={missing if missing else 'none'}"
    return TestResult("TEST 5 — Chapter coverage", passed, details), counts


def _test_6_critical_content(chunks: list[dict]) -> tuple[TestResult, dict[str, int]]:
    checks = {
        "axillary nerve": 3,
        "deltoid": 5,
        "rotator cuff": 2,
        "biceps": 3,
    }
    counts = {k: sum(1 for c in chunks if k in c["text"].lower()) for k in checks}
    passed = all(counts[k] > checks[k] for k in checks)
    details = " ".join([f"{k}={v}" for k, v in counts.items()])
    return TestResult("TEST 6 — Critical content check", passed, details), counts


def _extract_props_preview(chunk_text: str) -> list[str]:
    import anthropic

    client = anthropic.Anthropic(api_key=os.environ.get("ANTHROPIC_API_KEY"))
    prompt = cfg.prompts.proposition_extraction.format(chunk_text=chunk_text)

    msg = client.messages.create(
        model=cfg.models.propositions,
        max_tokens=700,
        messages=[{"role": "user", "content": prompt}],
    )
    raw = msg.content[0].text.strip()
    lines = [ln.strip("-•* \t") for ln in raw.splitlines() if ln.strip()]
    return lines


def _test_7_proposition_preview(chunks: list[dict]) -> tuple[TestResult, dict]:
    # a) one muscle chunk
    muscle = next((c for c in chunks if c["element_type"] == "paragraph" and any(k in c["text"].lower() for k in ["deltoid", "biceps", "rotator cuff"])), None)

    # b) table-converted chunk if available (new parse_pdf flow may not have these)
    table_chunk = next((c for c in chunks if c.get("element_type") == "table"), None)

    # c) clinical concept chunk
    clinical = next((c for c in chunks if any(k in c["text"].lower() for k in ["injur", "disease", "syndrome", "damage", "sprain", "clinical"])), None)

    # fallback for b) if no table chunks exist in this architecture
    if table_chunk is None:
        table_chunk = next((c for c in chunks if c["element_type"] == "paragraph_overlap"), None)

    samples = {
        "a_muscle": muscle,
        "b_table_or_overlap": table_chunk,
        "c_clinical": clinical,
    }

    previews = {}
    ok = True
    for key, c in samples.items():
        if c is None:
            previews[key] = {"error": "No suitable chunk found"}
            ok = False
            continue
        props = _extract_props_preview(c["text"])
        previews[key] = {
            "chunk_id": c["chunk_id"],
            "element_type": c["element_type"],
            "text": c["text"],
            "propositions": props,
        }

    return TestResult("TEST 7 — Proposition preview", ok, "generated previews for 3 chunk types"), previews


def _test_8_overlap_check(chunks: list[dict]) -> TestResult:
    overlaps = [c for c in chunks if c.get("element_type") == "paragraph_overlap"]
    base = {c["chunk_id"]: c for c in chunks if c.get("element_type") == "paragraph"}

    valid_original = 0
    valid_prefix = 0

    for c in overlaps:
        oid = c.get("original_chunk_id")
        sid = c.get("overlap_source_chunk_id")
        prefix = c.get("overlap_prefix", "")

        if oid in base:
            valid_original += 1
        if sid in base and prefix and prefix in base[sid]["text"]:
            valid_prefix += 1

    passed = (len(overlaps) > 0 and valid_original == len(overlaps) and valid_prefix == len(overlaps))
    details = (
        f"overlap_count={len(overlaps)} "
        f"valid_original_chunk_id={valid_original}/{len(overlaps)} "
        f"prefix_in_source={valid_prefix}/{len(overlaps)}"
    )
    return TestResult("TEST 8 — Overlap chunk check", passed, details)


def _test_9_section_level(chunks: list[dict], sections: list[dict]) -> tuple[TestResult, dict]:
    l1 = sum(1 for s in sections if int(s.get("source_level", 1)) == 1)
    l2 = sum(1 for s in sections if int(s.get("source_level", 1)) == 2)

    ax_chunks = [c for c in chunks if "axillary nerve" in c["text"].lower()]
    titles = sorted({c.get("section_title", "") for c in ax_chunks})

    passed = len(ax_chunks) > 3
    details = f"L1={l1} L2={l2} axillary_nerve_chunks={len(ax_chunks)}"
    payload = {"l1": l1, "l2": l2, "axillary_titles": titles}
    return TestResult("TEST 9 — Section-level check", passed, details), payload


def run_all_tests(chunks: list[dict], sections: list[dict]) -> dict:
    results: list[TestResult] = []

    t1 = _test_1_token_distribution(chunks)
    results.append(t1)

    t2, t2_rows = _test_2_topic_coherence(chunks)
    results.append(t2)

    t3 = _test_3_pronoun_starts(chunks)
    results.append(t3)

    t4 = _test_4_noise_detection(chunks)
    results.append(t4)

    t5, chapter_counts = _test_5_chapter_coverage(chunks)
    results.append(t5)

    t6, keyword_counts = _test_6_critical_content(chunks)
    results.append(t6)

    t7, previews = _test_7_proposition_preview(chunks)
    results.append(t7)

    t8 = _test_8_overlap_check(chunks)
    results.append(t8)

    t9, t9_payload = _test_9_section_level(chunks, sections)
    results.append(t9)

    all_pass = all(r.passed for r in results)

    return {
        "all_pass": all_pass,
        "results": results,
        "topic_rows": t2_rows,
        "chapter_counts": chapter_counts,
        "keyword_counts": keyword_counts,
        "previews": previews,
        "section_payload": t9_payload,
    }


